#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= ' ' && str[i] <= '?') || (str[i] >= '`' && str[i] <= '|')
			|| (str[i] == '\0'))
		{
			i++;
		}
		else
		{
			return (0);
		}
		return (1);
	}
}